#include "Mouse.h"

void Mouse::OnMouseMove(int new_x, int new_y) noexcept
{
	x = new_x;
	y = new_y;
	buffer.push(Mouse::Event(Mouse::Event::Type::Move, *this));
	TrimBuffer();
}

